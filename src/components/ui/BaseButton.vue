<template>
  <button v-if="!link" :class="mode">
    <slot></slot>
  </button>
  <router-link v-else :to="to" :class="mode">
    <slot></slot>
  </router-link>
</template>

<script>
export default {
  props: {
    mode: {
      type: String,
      required: false,
      default: null, // bind mode 可以方便切換 css class
    },
    link: {
      type: Boolean,
      required: false,
      default: false, // 基本以button顯示, 如果不是button 就以link顯示
    },
    to: {
      type: String,
      required: false,
      default: '/', // 如果是link的話就bind '/' 以便導向至其他頁面
    },
  },
};
</script>

